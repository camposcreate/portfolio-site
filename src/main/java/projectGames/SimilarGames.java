package projectGames;

public class SimilarGames {
    private String coverImage;
    private String coverTitle;
    private String releaseDate;

    public String getCoverImages() { return coverImage; }
    public void setCoverImages(String coverImage) { this.coverImage = coverImage; }
    public String getCoverTitles() { return coverTitle; }
    public void setCoverTitle(String coverTitle) { this.coverTitle = coverTitle; }
    public String getReleaseDate() { return releaseDate; }
    public void setReleaseDate(String releaseDate) { this.releaseDate = releaseDate; }
}
